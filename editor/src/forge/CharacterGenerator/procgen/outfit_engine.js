const PSYCHRONIC_ZONE_MASK_PREVIEW_SHIFT = { x: 1, y: -7 };
const PSYCHRONIC_ZONE_MASK_POINT_CACHE = new Map();

function psychronicZoneMaskRanges(zone, direction, frame) {
    const key = `${direction}:${frame || 0}`;
    return PSYCHRONIC_ZONE_MASKS[zone] && PSYCHRONIC_ZONE_MASKS[zone][key] || null;
}

function psychronicZoneMaskContains(zone, direction, frame, x, y) {
    const px = x + PSYCHRONIC_ZONE_MASK_PREVIEW_SHIFT.x;
    const py = y + PSYCHRONIC_ZONE_MASK_PREVIEW_SHIFT.y;
    const key = `${zone}:${direction}:${frame || 0}`;
    let points = PSYCHRONIC_ZONE_MASK_POINT_CACHE.get(key);
    if (!points) {
        const ranges = psychronicZoneMaskRanges(zone, direction, frame);
        points = new Set();
        if (ranges) {
            for (const [yy, x0, x1] of ranges) {
                for (let xx = x0; xx <= x1; xx++) points.add(`${xx},${yy}`);
            }
        }
        PSYCHRONIC_ZONE_MASK_POINT_CACHE.set(key, points);
    }
    return points.has(`${px},${py}`);
}

function psychronicZoneMaskPixels(zone, direction, frame, p, params = {}) {
    const ranges = psychronicZoneMaskRanges(zone, direction, frame);
    if (!ranges || !p) return [];
    let yMin = Infinity, yMax = -Infinity;
    for (const [y] of ranges) { if (y < yMin) yMin = y; if (y > yMax) yMax = y; }
    const pixels = [];
    const litSide = direction === 2 ? -1 : 1;
    for (const [py, x0, x1] of ranges) {
        const w = Math.max(1, x1 - x0 + 1);
        const y = py - PSYCHRONIC_ZONE_MASK_PREVIEW_SHIFT.y;
        const rowT = (py - yMin) / Math.max(1, yMax - yMin);
        for (let px = x0; px <= x1; px++) {
            const x = px - PSYCHRONIC_ZONE_MASK_PREVIEW_SHIFT.x;
            const t = (px - x0) / Math.max(1, w - 1);
            const edge = px === x0 || px === x1 || py === yMin || py === yMax;
            const centre = Math.abs(t - 0.5) < 0.08;
            let letter;
            if (edge) letter = p.outline;
            else if (params.powerStrip && p.glow && centre && (py % 3 !== 0)) letter = p.glow;
            else if ((t - 0.5) * litSide > 0.18) letter = p.highlight || p.lit;
            else if (rowT > 0.78 || (params.banded && py % 4 === 1)) letter = p.shadow;
            else letter = p.base || p.lit;
            if (letter) pixels.push({ x, y, letter });
        }
    }
    return pixels;
}

function psychronicExtensionMaskZone(type) {
    return type === 'pauldron' ? 'shoulders' : type;
}

function psychronicAuthoredZones(direction, frame, x, y) {
    const zones = [];
    for (const zone of ['torso', 'belt', 'arms', 'hands', 'legs', 'boots']) {
        if (psychronicZoneMaskContains(zone, direction, frame, x, y)) zones.push(zone);
    }
    return zones;
}

function classifyPixelPsychronic(x, y, ctx, anchors, SKIN, direction) {
    const { bands, bodyBbox } = anchors;
    const frame = ctx.frame || 0;
    const hasBeltMask = !!psychronicZoneMaskRanges('belt', direction, frame);
    const hasArmsMask = !!psychronicZoneMaskRanges('arms', direction, frame);
    const hasTorsoMask = !!psychronicZoneMaskRanges('torso', direction, frame);
    const hasLegsMask = !!psychronicZoneMaskRanges('legs', direction, frame);
    const authored = psychronicAuthoredZones(direction, frame, x, y);
    if (authored.length) return authored;
    if (y < bands.headBottom) return 'head';
    if (y < bands.beltTop) {
        if (!hasArmsMask && (direction === 0 || direction === 3)) {
            const armBand = y > bands.shoulderY + 8;
            const sideCut = Math.max(14, anchors.torso.halfWidth * 0.58);
            if (armBand && Math.abs(x - bodyBbox.centreX) > sideCut) return 'arms';
        } else if (!hasArmsMask) {
            const lowerArmBand = y > bands.shoulderY + 18;
            const ext = anchors.rowExtent && anchors.rowExtent[y];
            if (lowerArmBand && ext) {
                const frontEdge = direction === 1 ? ext[0] : ext[1];
                if (Math.abs(x - frontEdge) <= 6) return 'arms';
            }
        }
        return hasTorsoMask ? null : 'torso';
    }
    if (y < bands.beltBottom) return hasBeltMask ? (hasTorsoMask ? null : 'torso') : 'belt';
    if (y < bands.bootTop) return hasLegsMask ? null : 'legs';
    return 'boots';
}

