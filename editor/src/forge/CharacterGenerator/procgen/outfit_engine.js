// ── Palette builder ───────────────────────────────────────────────────────
// Collects every family + accent referenced by the config, assigns letters
// once per family/accent (so a steel helmet and steel breastplate share the
// steel ramp), and returns { palette, zonePalettes, extPalettes }.
function buildPalette(config) {
    const palette = {};            // letter → { hex, material }
    const ramps = {};              // familyName → { outline, shadow, ... }
    const accents = {};            // accentName → { glow, dim }
    let next = 0;
    const take = () => {
        if (next >= LETTER_POOL.length) throw new Error('Ran out of palette letters');
        return LETTER_POOL[next++];
    };
    function ramp(fam) {
        if (!FAMILIES[fam]) throw new Error('Unknown family: ' + fam);
        if (!ramps[fam]) {
            const r = {};
            FAMILIES[fam].forEach((hex, i) => {
                const letter = take();
                r[ROLE_NAMES[i]] = letter;
                palette[letter] = { hex, material: '' };
            });
            ramps[fam] = r;
        }
        return ramps[fam];
    }
    function accent(name) {
        if (!name) return null;
        if (!ACCENTS[name]) throw new Error('Unknown accent: ' + name);
        if (!accents[name]) {
            const a = ACCENTS[name];
            const glow = take(); palette[glow] = { hex: a.glow, material: '' };
            const dim  = take(); palette[dim]  = { hex: a.dim,  material: '' };
            accents[name] = { glow, dim };
        }
        return accents[name];
    }
    // Per-zone palette = the family's role letters + optional accent letters.
    function pzFor(spec) {
        if (!spec || !spec.family) return null;
        const r = ramp(spec.family);
        const a = accent(spec.accent);
        return Object.assign({}, r, a ? { glow: a.glow, dim: a.dim } : {});
    }
    const zonePalettes = {};
    for (const [zone, spec] of Object.entries(config.zones || {})) {
        if (spec && spec.enabled === false) continue;
        zonePalettes[zone] = pzFor(spec);
    }
    const extPalettes = (config.extensions || []).map(ext => pzFor(ext));
    return { palette, zonePalettes, extPalettes };
}

// ── Body skin-letter classification ───────────────────────────────────────
function detectSkinLetters(bodyPalette) {
    const out = new Set();
    for (const [letter, entry] of Object.entries(bodyPalette)) {
        const hex = (entry.hex || entry || '').replace('#', '');
        const r = parseInt(hex.slice(0, 2), 16);
        const g = parseInt(hex.slice(2, 4), 16);
        const b = parseInt(hex.slice(4, 6), 16);
        if (r > 150 && r >= g && g > b && r - b > 30) out.add(letter);
    }
    return out;
}

function templateRowWidth(row) {
    return (typeof row === 'string' || Array.isArray(row)) ? row.length : 0;
}

function templateCell(row, x) {
    if (typeof row === 'string' || Array.isArray(row)) return row[x];
    return null;
}

function templateBlank(ch) {
    return ch === undefined || ch === null || ch === '' || ch === ' ' || ch === '.' || ch === -1;
}

function templatePadRow(row, width) {
    if (Array.isArray(row)) {
        const out = row.slice();
        while (out.length < width) out.push(null);
        return out;
    }
    return String(row || '').padEnd(width, ' ');
}

// ── Per-frame silhouette analysis (edge distances, run length, anchors) ───
function edgeDistances(frame, W, H) {
    const dT = mat(H, W, 99), dB = mat(H, W, 99), dL = mat(H, W, 99), dR = mat(H, W, 99);
    const solid = (x, y) => {
        if (y < 0 || y >= H || x < 0 || x >= W) return false;
        const ch = templateCell(frame[y], x);
        return !templateBlank(ch);
    };
    for (let y = 0; y < H; y++) for (let x = 0; x < W; x++) {
        if (!solid(x, y)) continue;
        if (!solid(x, y - 1)) dT[y][x] = 0; else if (dT[y - 1][x] < 99) dT[y][x] = dT[y - 1][x] + 1;
        if (!solid(x - 1, y)) dL[y][x] = 0; else if (dL[y][x - 1] < 99) dL[y][x] = dL[y][x - 1] + 1;
    }
    for (let y = H - 1; y >= 0; y--) for (let x = W - 1; x >= 0; x--) {
        if (!solid(x, y)) continue;
        if (!solid(x, y + 1)) dB[y][x] = 0; else if (dB[y + 1][x] < 99) dB[y][x] = dB[y + 1][x] + 1;
        if (!solid(x + 1, y)) dR[y][x] = 0; else if (dR[y][x + 1] < 99) dR[y][x] = dR[y][x + 1] + 1;
    }
    return { dT, dB, dL, dR };
}
function mat(h, w, v) { return Array.from({ length: h }, () => new Array(w).fill(v)); }

function computeRunLen(frame, H, W) {
    const out = mat(H, W, 0);
    for (let y = 0; y < H; y++) {
        const row = templatePadRow(frame[y], W);
        let x = 0;
        while (x < W) {
            if (templateBlank(row[x])) { x++; continue; }
            let end = x;
            while (end < W && !templateBlank(row[end])) end++;
            for (let i = x; i < end; i++) out[y][i] = end - x;
            x = end;
        }
    }
    return out;
}
function frameBbox(frame) {
    let minY = Infinity, maxY = -1, minX = Infinity, maxX = -1;
    for (let y = 0; y < frame.length; y++) {
        const row = frame[y];
        for (let x = 0; x < templateRowWidth(row); x++) if (!templateBlank(templateCell(row, x))) {
            if (y < minY) minY = y; if (y > maxY) maxY = y;
            if (x < minX) minX = x; if (x > maxX) maxX = x;
        }
    }
    return { minX, maxX, minY, maxY };
}
// Per-row half width and centre — lets painters follow the body's true edges.
function rowEdges(frame, y) {
    const row = frame[y] || '';
    let minX = Infinity, maxX = -1;
    for (let x = 0; x < templateRowWidth(row); x++) if (!templateBlank(templateCell(row, x))) {
        if (x < minX) minX = x; if (x > maxX) maxX = x;
    }
    if (maxX < 0) return null;
    return { minX, maxX, cx: (minX + maxX) / 2, half: (maxX - minX) / 2 };
}
function zoneHalfWidth(frame, yStart, yEnd, centreX) {
    let max = 0;
    for (let y = yStart; y <= yEnd; y++) {
        const row = frame[y] || '';
        for (let x = 0; x < templateRowWidth(row); x++) if (!templateBlank(templateCell(row, x))) {
            const d = Math.abs(x - centreX); if (d > max) max = d;
        }
    }
    return max;
}
function analyzeFrame(frame) {
    const H = frame.length;
    let W = 0;
    for (const row of frame) if (templateRowWidth(row) > W) W = templateRowWidth(row);
    const bb = frameBbox(frame);
    const cx = Math.round((bb.minX + bb.maxX) / 2);
    const bodyH = bb.maxY - bb.minY + 1;
    const headBottom  = bb.minY + Math.round(bodyH * 0.42);
    const shoulderY   = headBottom;
    const chestBottom = bb.minY + Math.round(bodyH * 0.65);
    const beltTop     = bb.minY + Math.round(bodyH * 0.63);
    const beltBottom  = bb.minY + Math.round(bodyH * 0.72);
    const kneeY       = bb.minY + Math.round(bodyH * 0.70);
    const bootTop     = bb.minY + Math.round(bodyH * 0.78);
    const ankleY      = bb.minY + Math.round(bodyH * 0.95);
    return {
        bodyBbox: { ...bb, centreX: cx, H, W, height: bodyH },
        head:  { minY: bb.minY, maxY: headBottom, centreX: cx, halfWidth: zoneHalfWidth(frame, bb.minY, headBottom - 2, cx) },
        torso: { minY: shoulderY, maxY: chestBottom, centreX: cx, halfWidth: zoneHalfWidth(frame, shoulderY + 2, chestBottom, cx),
                 shoulderHalfWidth: zoneHalfWidth(frame, shoulderY - 1, shoulderY + 2, cx) },
        belt:  { minY: beltTop, maxY: beltBottom, centreX: cx, halfWidth: zoneHalfWidth(frame, beltTop, beltBottom, cx) },
        legs:  { minY: beltBottom, maxY: bootTop, centreX: cx, kneeY, halfWidth: zoneHalfWidth(frame, beltBottom, bootTop, cx) },
        boots: { minY: bootTop, maxY: bb.maxY, centreX: cx, halfWidth: zoneHalfWidth(frame, bootTop, bb.maxY, cx) },
        bands: { headBottom, shoulderY, chestBottom, beltTop, beltBottom, kneeY, bootTop, ankleY }
    };
}

