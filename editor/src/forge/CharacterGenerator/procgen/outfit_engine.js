// Find the face (skin) band inside the head zone for this frame, so helmets
// can anchor the visor to the real eye-line instead of guessing from height.
function faceBand(frame, anchors, SKIN) {
    let top = Infinity, bottom = -1, sumX = 0, n = 0;
    for (let y = anchors.head.minY; y <= anchors.bands.headBottom; y++) {
        const row = frame[y] || '';
        for (let x = 0; x < templateRowWidth(row); x++) {
            if (SKIN.has(String(templateCell(row, x)))) {
                if (y < top) top = y; if (y > bottom) bottom = y;
                sumX += x; n++;
            }
        }
    }
    if (bottom < 0) return { top: anchors.head.minY, bottom: anchors.bands.headBottom, cx: anchors.head.centreX, found: false };
    return { top, bottom, cx: Math.round(sumX / n), found: true };
}

// Detect the arm clusters per frame: skin pixels in short, off-centre runs in
// the arm Y-band. Returns per-side {minX,maxX,minY,maxY, byRow:{y:[x0,x1]}} so
// armour can be stamped as a CHUNKY shape that bulges beyond the thin arm —
// the way the artist's armguards sit on the body (see armguard-02).
function detectArmClusters(frame, anchors, SKIN) {
    const { bands, bodyBbox } = anchors;
    const cx = bodyBbox.centreX;
    const torsoHW = anchors.torso.halfWidth || 12;
    const sides = { left: [], right: [] };
    for (let y = bands.shoulderY; y <= bands.beltBottom + 4; y++) {
        const row = frame[y] || '';
        let x = 0;
        const rowW = templateRowWidth(row);
        while (x < rowW) {
            if (!SKIN.has(String(templateCell(row, x)))) { x++; continue; }
            let e = x; while (e < rowW && SKIN.has(String(templateCell(row, e)))) e++;
            const len = e - x, mid = (x + e - 1) / 2;
            // Short run, sitting out toward a shoulder = an arm, not the torso.
            if (len <= 16 && Math.abs(mid - cx) > torsoHW * 0.45) {
                (mid < cx ? sides.left : sides.right).push([y, x, e - 1]);
            }
            x = e;
        }
    }
    const pack = (arr) => {
        if (arr.length < 3) return null;
        let minX = 1e9, maxX = -1, minY = 1e9, maxY = -1;
        const byRow = {};
        for (const [y, x0, x1] of arr) {
            minX = Math.min(minX, x0); maxX = Math.max(maxX, x1);
            minY = Math.min(minY, y); maxY = Math.max(maxY, y);
            if (!byRow[y] || x1 - x0 > byRow[y][1] - byRow[y][0]) byRow[y] = [x0, x1];
        }
        return { minX, maxX, minY, maxY, byRow };
    };
    return { left: pack(sides.left), right: pack(sides.right) };
}

// ── Pixel → zone classification ───────────────────────────────────────────
function classifyPixel(x, y, ctx, anchors, SKIN, direction) {
    const { bands } = anchors;
    if (y < bands.headBottom) return 'head';
    // Arm-by-skin detection only makes sense head-on (front/back), where the two
    // arms sit clearly out at the torso sides. In PROFILE the arm and bare chest
    // merge, so this mis-fires on back skin and paints a bouncing bump — skip it
    // and let the torso cover the back; the gauntlet extension draws the one
    // visible front arm instead.
    if (direction !== 1 && direction !== 2 &&
        SKIN.has(String(ctx.srcChar)) && ctx.runLen < 14 && y >= bands.shoulderY && y <= bands.beltBottom) return 'arms';
    if (y < bands.beltTop)    return 'torso';
    if (y < bands.beltBottom) return 'belt';
    if (y < bands.bootTop)    return 'legs';
    return 'boots';
}

